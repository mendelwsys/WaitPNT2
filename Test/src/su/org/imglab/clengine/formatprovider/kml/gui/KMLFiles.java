package su.org.imglab.clengine.formatprovider.kml.gui;

public class KMLFiles
{
	private String kmlfile;
	private String stylefile;
	private String indexfile;

	public KMLFiles()
	{
	}

	public String getKmlfile()
	{
		return kmlfile;
	}

	public void setKmlfile(final String kmlfile)
	{
		this.kmlfile = kmlfile;
	}

	public String getStylefile()
	{
		return stylefile;
	}

	public void setStylefile(final String stylefile)
	{
		this.stylefile = stylefile;
	}

	public String getIndexfile()
	{
		return indexfile;
	}

	public void setIndexfile(final String indexfile)
	{
		this.indexfile = indexfile;
	}
}